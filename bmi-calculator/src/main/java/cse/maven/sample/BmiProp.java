package cse.maven.sample;

public class BmiProp {
	private final float height;
	private final float weight;
	
	public BmiProp(float height, float weight) {
		super();
		this.height = height;
		this.weight = weight;
	}

	public float getHeight() {
		return height;
	}

	public float getWeight() {
		return weight;
	}
	
	
	
}
